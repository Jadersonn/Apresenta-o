public class Carro {

    private int carroId;
    private String nomeCarro;
    private String descricaoCarro;

    /**
     * @return the carroId
     */
    public int getCarroId() {
        return carroId;
    }

    /**
     * @param carroId the carroId to set
     */
    public void setCarroId(int carroId) {
        this.carroId = carroId;
    }

    /**
     * @return the nomeCarro
     */
    public String getNomeCarro() {
        return nomeCarro;
    }

    /**
     * @param nomeCarro the nomeCarro to set
     */
    public void setNomeCarro(String nomeCarro) {
        this.nomeCarro = nomeCarro;
    }

    /**
     * @return the descricaoCarro
     */
    public String getDescricaoCarro() {
        return descricaoCarro;
    }

    /**
     * @param descricaoCarro the descricaoCarro to set
     */
    public void setDescricaoCarro(String descricaoCarro) {
        this.descricaoCarro = descricaoCarro;
    }

}
