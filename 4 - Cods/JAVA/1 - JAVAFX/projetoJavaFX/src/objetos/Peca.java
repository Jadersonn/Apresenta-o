public class Peca {

    private int pecaId;
    private String nomePeca;
    private String categoria;
    private String idGeralPeca;

    /**
     * @return the pecaId
     */
    public int getPecaId() {
        return pecaId;
    }

    /**
     * @param pecaId the pecaId to set
     */
    public void setPecaId(int pecaId) {
        this.pecaId = pecaId;
    }

    /**
     * @return the nomePeca
     */
    public String getNomePeca() {
        return nomePeca;
    }

    /**
     * @param nomePeca the nomePeca to set
     */
    public void setNomePeca(String nomePeca) {
        this.nomePeca = nomePeca;
    }

    /**
     * @return the categoria
     */
    public String getCategoria() {
        return categoria;
    }

    /**
     * @param categoria the categoria to set
     */
    public void setCategoria(String categoria) {
        this.categoria = categoria;
    }

    /**
     * @return the idGeralPeca
     */
    public String getIdGeralPeca() {
        return idGeralPeca;
    }

    /**
     * @param idGeralPeca the idGeralPeca to set
     */
    public void setIdGeralPeca(String idGeralPeca) {
        this.idGeralPeca = idGeralPeca;
    }

}
